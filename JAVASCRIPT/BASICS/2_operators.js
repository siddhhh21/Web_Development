let num1 = 2;
let num2 = 3;

var num3 = 3;

// Arithmetic Operators
console.log("Addition:", num1 + num2);
console.log("Subtraction:", num1 - num2);
console.log("Multiplication:", num3 * num1);
console.log("Division:", num3 / num2);
console.log("Modulus:", num3 % num1);

// Comparison Operators
console.log("num1 == num2 :", num1 == num2);   // Equal to
console.log("num2 == num3 :", num2 == num3);

console.log("num1 != num2 :", num1 != num2);   // Not equal to
console.log("num1 > num2  :", num1 > num2);    // Greater than
console.log("num1 < num2  :", num1 < num2);    // Less than
console.log("num2 >= num3 :", num2 >= num3);   // Greater than or equal to
console.log("num1 <= num2 :", num1 <= num2);   // Less than or equal to

// Strict Comparison Operators
console.log("num2 === num3 :", num2 === num3); // Strict equality
console.log("num1 !== num2 :", num1 !== num2); // Strict inequality